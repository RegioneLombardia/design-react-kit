type Story = StoryObj<typeof MegamenuItem>;

function showCloser(show: boolean, id: string) {
  const closer: HTMLElement | null = document.getElementById(id)
  if (closer) {
    if (show) {
      closer.classList.add("show")
    } else {
      closer.classList.remove("show")
    }
    closer.blur()
  }
}

